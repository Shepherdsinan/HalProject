SELECT 'SEVK' AS [GÝRÝÞ ÇIKIÞ], STY_SEVK_HAREKET.TARIH, STY_SEVK_HAREKET.SERI, STY_SEVK_HAREKET.SIRA, STY_DEPO.DEPO_NO AS [CIKIS DEPO NO], STY_DEPO.DEPO_TANIM AS [CIKIS DEPO TANIM], STY_DEPO_1.DEPO_NO AS [GIRIS DEPO NO], 
                  STY_DEPO_1.DEPO_TANIM AS [GIRIS DEPO TANIM], STY_STOK.KODU, STY_STOK.ADI, STY_KASA.NO, STY_KASA.TANIM,
				  (CASE WHEN  STY_KASA.IADE=1 THEN 'ÝADELÝ KASA' ELSE 'ÝADESÝZ KASA' END) AS [KASA TIPI],
				    STY_SEVK_HAREKET.KASA_MIKTAR, STY_SEVK_HAREKET.URUN_MIKTAR, 
                  STY_SEVK_HAREKET.BIRIM_FIYAT, STY_SEVK_HAREKET.TOPLAM_TUTAR
FROM     STY_SEVK_HAREKET INNER JOIN
                  STY_STOK ON STY_SEVK_HAREKET.STOK_KODU = STY_STOK.KODU INNER JOIN
                  STY_DEPO ON STY_SEVK_HAREKET.CIKIS_DEPO_NO = STY_DEPO.DEPO_NO INNER JOIN
                  STY_DEPO AS STY_DEPO_1 ON STY_SEVK_HAREKET.GIRIS_DEPO_NO = STY_DEPO_1.DEPO_NO INNER JOIN
                  STY_KASA ON STY_SEVK_HAREKET.KASA_NO = STY_KASA.NO
				  WHERE TARIH >=CONVERT(DATETIME, '@D@Baþlangýç Tarihi@ 00:00:00', 102) 
and TARIH<=CONVERT(DATETIME, '@D@Bitiþ Tarihi@ 23:59:59', 102) and
STY_SEVK_HAREKET.STOK_KODU like '@S@Stok Kodu@'